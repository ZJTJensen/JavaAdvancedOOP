public class Pokemon{
    public int health;
    public String name;
    public String type;
    public Pokemon(){

    }

    
    public void setHealth(int health) {
		this.health = health;
	}
	public int getHealth() {
		return health;
    }
    public void setName(String name) {
		this.name = name;
	}
	public String getName() {
		return name;
    }
    public void setType(String type) {
		this.type = type;
	}
	public String getType() {
		return type;
	}
}